const translations = { ko, en } as const;

export function useT() {
  const language = useSettingsStore((s) => s.language);
  return (key: TranslationKey, params?: Record<string, string | number>) => {
    const lang = language in translations ? language : "ko";
    let text: string = (translations[lang as keyof typeof translations] as Record<string, string>)[key]
      ?? (translations.ko as Record<string, string>)[key]
      ?? key;
    if (params) {
      Object.entries(params).forEach(([k, v]) => {
        text = text.replace(`{${k}}`, String(v));
      });
    }
    return text;
  };
}
